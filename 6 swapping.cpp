#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "Enter Two Numbers : " << endl;
    cin >> a >> b;
    cout << "Before Swapping : " << endl;
    cout << "A = " << a << endl;
    cout << "B = " << b << endl;
    cout << " After Swapping : " << endl;
    cout << "A = " << b << endl;
    cout << "B = " << a << endl;
    return 0;
}